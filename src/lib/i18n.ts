const DICTS: Record<Lang, Dict> = { en, es };

/** English keys missing a translation in `lang`'s dictionary (for tests/QA). */
export function untranslatedKeys(lang: Lang): string[] {
  const dict = DICTS[lang] ?? {};
  return Object.keys(en).filter((k) => !(k in dict));
}

/** Translate `key` into `lang`, interpolating `{param}` placeholders. Falls back
 *  to English, then to the raw key, so a missing translation never crashes. */
export function translate(
  lang: Lang,
  key: string,
  params?: Record<string, string | number>,
): string {
  let s = DICTS[lang]?.[key] ?? en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}
